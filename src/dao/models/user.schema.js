import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
  },
  email: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
  },
  password: {
    type: String,
  },
  is_admin: {
    type: Boolean,
    required: true,
  },
});

export default mongoose.model("Users", UserSchema);
